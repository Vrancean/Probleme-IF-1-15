y = int(input("dati numarul de petale ale unei flori: "))
if (y % 5 == 0):
    print("Nu ma iubeste de loc")
elif (y % 5 == 1):
    print("Ma iubeste un pic")
elif (y % 5 == 2):
    print("Ma iubeste mult")
elif (y % 5 == 3):
    print("Ma iubeste cu pasiune")
elif (y % 5 == 4):
    print("Ma iubeste la nebunie")